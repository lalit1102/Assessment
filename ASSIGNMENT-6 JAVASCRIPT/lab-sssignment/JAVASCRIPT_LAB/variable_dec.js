// String
let myString = "Hello, JavaScript!";
console.log("Value:", myString, "| Type:", typeof myString);

// Number
let myNumber = 42;
console.log("Value:", myNumber, "| Type:", typeof myNumber);

// Boolean
let myBoolean = true;
console.log("Value:", myBoolean, "| Type:", typeof myBoolean);

// Null
let myNull = null;
console.log("Value:", myNull, "| Type:", typeof myNull);  // Special case: type is "object"

// Undefined
let myUndefined;
console.log("Value:", myUndefined, "| Type:", typeof myUndefined);

// Bigint
let bigNumber = 1234567890123456789012345678901234567890n;
console.log("BigInt 1:", bigNumber);